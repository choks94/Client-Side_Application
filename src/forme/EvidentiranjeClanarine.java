    public void vratiPodatke() {
        ClanoviForma cf = (ClanoviForma) this.getParent();
        int indeksSelektovanogClana = cf.vratiIndeksSelektovanogClana();
        selektovaniClan = cf.vratiSelektovanogClana(indeksSelektovanogClana);
        lblClan.setText(selektovaniClan.getIme() + " " + selektovaniClan.getPrezime());

        KlijentskiZahtev kz = new KlijentskiZahtev();
        Clanarina cl = new Clanarina();
        cl.setClan(selektovaniClan);
        kz.setParametar(cl);
        kz.setOperacija(operacije.Operacija.VRATI_CLANARINU);

        komunikacija.Komunikacija.getInstanca().posaljiKlijentskiZahtev(kz);
        ServerskiOdgovor so = komunikacija.Komunikacija.getInstanca().primiServerskiOdgovor();

        c = (Clanarina) so.getOdgovor();
        if (c == null) {
            lblDatum.setText("Nema podataka o clanarini");
            return;
        }
        Date sadasnjiDatun = new Date();
        if (c.getDatumDo().before(sadasnjiDatun)) {
            lblDatum.setText("Clanarina je istekal");
        } else {
            SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");
            String datumString = sdf.format(c.getDatumDo());
            lblDatum.setText(datumString);
        }
    }


