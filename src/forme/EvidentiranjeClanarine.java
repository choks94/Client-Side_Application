    private void btnEvidentirajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEvidentirajActionPerformed

        double cena = 0.0;
        try {
            cena = Double.parseDouble(txtCena.getText());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Molimo Vas da unosite validne vrednosti za cenu");
            return;
        }

        int brMeseci = 0;
        if ((int) spnBrojMeseci.getValue() < 1) {
            JOptionPane.showMessageDialog(this, "Evidentiranje clanarine mora biti za bar jedan mesec");
            return;
        }

        brMeseci = (int) spnBrojMeseci.getValue();

        Date datumOd = new Date();
        Calendar kalendar = Calendar.getInstance();
        if (c == null) {
            kalendar.setTime(datumOd);
            kalendar.add(Calendar.MONTH, brMeseci);
        } else {
            if (c.getDatumDo().before(datumOd) || c.getDatumDo() == null) {
                kalendar.setTime(datumOd);
                kalendar.add(Calendar.MONTH, brMeseci);
            } else {
                datumOd = c.getDatumDo();
                kalendar.setTime(c.getDatumDo());
                kalendar.add(Calendar.MONTH, brMeseci);
            }
        }

        Date datumNoveClanarine = kalendar.getTime();
        ClanoviForma cf = (ClanoviForma) this.getParent();
        Radnik radnik = cf.vratiUlogovanogRadnika();
        Clanarina novaClanarina = new Clanarina(-1, datumOd, datumNoveClanarine, cena, radnik, selektovaniClan);
        cf.setEvidentiranaClanarina(novaClanarina);
        PotvrdaOEvidencijiClanarine poec = new PotvrdaOEvidencijiClanarine(cf, true);
        poec.setEc(this);
        poec.pack();
        poec.setVisible(true);

    }//GEN-LAST:event_btnEvidentirajActionPerformed

